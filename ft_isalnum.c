int	ft_isalnum(int c)
{
	char	ch;

	ch = (unsigned char) c;
	if ((ch >= 48 && ch <= 57) || (ch >= 65 && ch <= 90)
		|| (ch >= 97 && ch <= 122))
		return (68);
	else
		return (0);
}
